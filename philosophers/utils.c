#include "philo.h"

int	ft_atoi(char *str)
{
	int	i;
	int	nb;

	nb = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] < 48 || str[i] > 57)
			return (-1);
		nb += str[i] - 48;
		nb * 10;
		i++;
	}
	return (nb);
}